async function populateFoods() {
  console.log('Starting to populate foods...');

  for (const food of defaultFoods) {
    const nameLower = food.name.toLowerCase();
    const item = {
      PK: `FOOD#${food.id}`,
      SK: `FOOD#${food.id}`,
      GSI1PK: 'FOOD',
      GSI1SK: `${nameLower}#${food.id}`,
      EntityType: 'FOOD',
      FoodId: food.id,
      Name: food.name,
      NameLower: nameLower,
      Brand: food.brand,
      Category: JSON.stringify(food.category),
      Subcategory: food.subcategory,
      NutritionFacts: JSON.stringify(food.nutritionFacts),
      ServingSize: food.serving_size,
      ServingUnit: food.serving_unit,
      CommonServings: JSON.stringify(food.commonServings),
      Allergens: JSON.stringify(food.allergens),
      DietaryTags: JSON.stringify(food.dietaryTags),
      Verified: food.verified,
      IsActive: food.isActive,
      CreatedAt: new Date().toISOString(),
      UpdatedAt: new Date().toISOString(),
    };

    try {
      await docClient.send(
        new PutCommand({
          TableName: tableName,
          Item: item,
        })
      );
      console.log(`✅ Added food: ${food.name}`);
    } catch (error) {
      console.error(`❌ Failed to add food ${food.name}:`, error);
    }
  }

  console.log('✅ Food population completed!');
}

